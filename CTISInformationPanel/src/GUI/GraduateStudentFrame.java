    private void btnAddCoursesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCoursesActionPerformed
        if (txtId.getText().isEmpty() || txtName.getText().isEmpty() || txtYear.getText().isEmpty()
                || txtSurname.getText().isEmpty() || txtYearOfAdmission.getText().isEmpty()
                || txtYear.getText().isEmpty()) {
            labelAlert.setText("Please fill all the fields!");
        } else {
            if (StudentSys.searchStudent(Integer.parseInt(txtId.getText())) != null) {
                labelAlert.setText("Student already exists!");
            } else {
                GraduateStudent g = new GraduateStudent(txtId.getText(),
                        txtName.getText(), txtSurname.getText(), txtYearOfAdmission.getText(),
                        txtTheory.getText(), txtYear.getText());
                StudentSys.addStudent(g);
                AddCoursesFrame courseFrame = new AddCoursesFrame(null, g, this);
                courseFrame.setVisible(true);
                setVisible(false);
                labelAlert.setText(txtName.getText() + " " + txtSurname.getText() + " is added");
                clear();
            }
        }
    }//GEN-LAST:event_btnAddCoursesActionPerformed

